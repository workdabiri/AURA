/**
 * Reject off-plan-only fields on a non-off-plan property (D-36). Runs when `market_type`
 * is known on the payload; the publish checklist re-checks the merged record at publish.
 */
function offPlanRefine(
  value: Record<string, unknown>,
  ctx: z.RefinementCtx,
  marketType: unknown
): void {
  if (marketType !== undefined && marketType !== 'off_plan') {
    for (const field of OFF_PLAN_FIELDS) {
      const v = value[field]
      if (v !== undefined && v !== null && v !== '') {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          path: [field],
          message: `${field} is allowed only when market_type is off_plan.`,
        })
      }
    }
  }
}

/** Create a DRAFT property. The minimal required set lets the slug derive and the row insert. */
export const propertyCreateSchema = z
  .object({
    ...editableShape,
    title: i18nRequiredEn,
    transaction_type: z.enum(TRANSACTION_TYPES),
    market_type: z.enum(MARKET_TYPES),
    property_type: z.enum(PROPERTY_TYPES),
    location_label: z.string().trim().min(1).max(200),
    size_sqft: nonNegNumber,
    reference_number: referenceNumberOverrideSchema.optional(),
  })
  .superRefine((value, ctx) => {
    offPlanRefine(value, ctx, value.market_type)
    if (slugifyTitleEn(value.title.en) === '') {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        path: ['title', 'en'],
        message: 'Title must contain letters or numbers so a URL slug can be derived.',
      })
    }
  })

export type PropertyCreateInput = z.infer<typeof propertyCreateSchema>

/**
 * Update an existing property. All editable fields optional; `publish: true` requests a
 * draft → published transition (gated by the publish checklist in the route/DAL). Slug,
 * reference number, publish_status, and ownership columns are intentionally NOT accepted —
 * slug/reference immutability (A-06) and the lifecycle (D-32) are enforced structurally.
 */
export const propertyUpdateSchema = z
  .object({
    ...editableShape,
    publish: z.boolean().optional(),
  })
  .superRefine((value, ctx) => {
    offPlanRefine(value, ctx, value.market_type)
  })

export type PropertyUpdateInput = z.infer<typeof propertyUpdateSchema>

// --- Update payload builder (slug/reference/status immutability backstop) ---------

/** Columns an admin update may NEVER write directly (immutability + lifecycle backstop). */
const NON_UPDATABLE_KEYS = new Set([
  'id',
  'slug',
  'reference_number',
  'publish_status',
  'published_at',
  'archived_at',
  'title_en',
  'views_count',
  'created_at',
  'created_by',
  'updated_at',
  'publish',
])

/**
 * Build the DB update object for a property: the provided editable fields only, with
 * `updated_by` stamped. Defensively strips any immutable/lifecycle key (slug, reference
 * number, publish_status, …) even if present — so slug can never change via update (A-06)
 * and the publish lifecycle cannot be driven through the generic patch.
 */
export function buildUpdatePayload(
  patch: Record<string, unknown>,
  actorUserId: string | null
): Record<string, unknown> {
  const out: Record<string, unknown> = {}
  for (const [key, value] of Object.entries(patch)) {
    if (NON_UPDATABLE_KEYS.has(key)) continue
    if (value === undefined) continue
    out[key] = value
  }
  out.updated_by = actorUserId
  return out
}

