// --- Reference number (D-47 / A-05) ----------------------------------------------

/** Default reference-number prefix (owner decision — AUTEX demo brand). */
export const REFERENCE_NUMBER_PREFIX = 'AUTEX'
/** Sequence is zero-padded to this width (e.g. `AUTEX-00041`). */
const REFERENCE_NUMBER_PAD = 5
/** Accepted admin override shape: uppercase alphanumerics + single hyphens, bounded. */
const REFERENCE_NUMBER_PATTERN = /^[A-Z0-9]+(?:-[A-Z0-9]+)*$/
const REFERENCE_NUMBER_MIN = 3
const REFERENCE_NUMBER_MAX = 40

/**
 * Format an auto reference number from a numeric sequence: `${prefix}-${padded}`.
 * Pure + deterministic; the DAL supplies the next sequence and handles uniqueness retry.
 */
export function formatReferenceNumber(
  sequence: number,
  prefix: string = REFERENCE_NUMBER_PREFIX
): string {
  const padded = String(Math.max(0, Math.trunc(sequence))).padStart(REFERENCE_NUMBER_PAD, '0')
  return `${prefix}-${padded}`
}

/** A validated admin override for the reference number (normalised to uppercase). */
export const referenceNumberOverrideSchema = z
  .string()
  .trim()
  .min(REFERENCE_NUMBER_MIN)
  .max(REFERENCE_NUMBER_MAX)
  .transform((v) => v.toUpperCase())
  .refine((v) => REFERENCE_NUMBER_PATTERN.test(v), {
    message: 'Reference number may contain only letters, numbers, and single hyphens.',
  })

// --- Slug (A-06) -----------------------------------------------------------------

/**
 * Derive a public slug from an English title: lowercase, non-alphanumerics → hyphens,
 * collapsed and trimmed, bounded to SLUG_MAX_LENGTH. Returns `''` when the title yields
 * nothing usable (callers treat that as a validation failure). Pure + deterministic; the
 * DAL appends a numeric suffix on collision (`-2`, `-3`, …).
 */
export function slugifyTitleEn(titleEn: string): string {
  const base = titleEn
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, SLUG_MAX_LENGTH)
    .replace(/-+$/g, '')
  return SLUG_PATTERN.test(base) ? base : ''
}

/**
 * Append a collision suffix to a base slug while staying within SLUG_MAX_LENGTH:
 * `marina-penthouse` → `marina-penthouse-2`. `n <= 1` returns the base unchanged.
 */
export function withSlugSuffix(base: string, n: number): string {
  if (n <= 1) return base
  const suffix = `-${n}`
  const room = SLUG_MAX_LENGTH - suffix.length
  return `${base.slice(0, room).replace(/-+$/g, '')}${suffix}`
}

// --- Lifecycle rules (D-32) ------------------------------------------------------

/** A property can be edited only while it is not archived (archived is terminal in MVP UI). */
export function canEditProperty(status: PublishStatus): boolean {
  return status !== 'archived'
}

/** Publishing is allowed only from `draft` (no re-publish, no unpublish — D-32). */
export function canPublish(status: PublishStatus): boolean {
  return status === 'draft'
}

/** Archiving is allowed from `draft` or `published`; archiving an archived row is a no-op error. */
export function canArchive(status: PublishStatus): boolean {
  return status === 'draft' || status === 'published'
}

/** A-06: the slug may change only while the property is a draft; immutable once published. */
export function canMutateSlug(status: PublishStatus): boolean {
  return status === 'draft'
}

