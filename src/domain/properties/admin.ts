/**
 * AURA-303 — admin property write contract (pure domain logic).
 *
 * Single source of truth for the admin create/update/duplicate shapes consumed by:
 *   - the admin API routes (`src/app/api/admin/properties/**`),
 *   - the admin DAL write functions (`src/dal/properties.dal.ts`),
 *   - the admin property form (`src/components/admin/PropertyForm.tsx`, client-side).
 *
 * PURE TypeScript: NO React, NO Supabase, NO DAL, NO services, NO I/O, NO `server-only`.
 * Every business rule lives here (not in JSX or the route), so handlers/forms never trust
 * raw input and the rules are unit-testable in Node.
 *
 * Locked decisions enforced here:
 *   - D-36 taxonomy (never overload `status`): explicit transaction/market/property/
 *     availability/price-visibility/rental/furnishing enums.
 *   - D-47 / A-05 reference number: configurable prefix (`AUTEX`) + padded sequence;
 *     auto-generated by default; optional admin override; uniqueness mandatory.
 *   - A-06 slug: derived from `title.en`, collision-suffixed, immutable after publish.
 *   - D-09 bedrooms: handled by the publish checklist (see ./publish), not required on a draft.
 *   - D-32 lifecycle: draft → published → archived; no hard delete; no published → draft
 *     (unpublish) in MVP.
 *
 * Publish-readiness (completeness) is NOT enforced here — a draft may be incomplete. It is a
 * separate concern in ./publish, evaluated only at publish time.
 */

